    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String texto = txtComentario.getText().trim();
        
        if (texto.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, digite um coment√°rio.");
            return;
        }

        Feedback novoFeedback = new Feedback();
        novoFeedback.setComentario(texto);
        novoFeedback.setCliente(this.clienteLogado);

        try {
            controller.salvar(novoFeedback);
            this.dispose(); // Fecha a tela
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

